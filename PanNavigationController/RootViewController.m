- (void)loadView{
    [super loadView];
    UIButton *pushButton = [UIButton buttonWithType:UIButtonTypeRoundedRect];
    pushButton.frame = CGRectMake(100, 100, 100, 50);
    [pushButton setTitle:@"Pop" forState:UIControlStateNormal];
    [pushButton addTarget:self action:@selector(pop:) forControlEvents:UIControlEventTouchUpInside];
    [self.view addSubview:pushButton];
    
    self.title = [NSString stringWithFormat:@"%d", self.navigationController.viewControllers.count];
}

